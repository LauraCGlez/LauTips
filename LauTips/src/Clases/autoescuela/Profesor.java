package Clases.autoescuela;

// Cada profesor tiene un nombre, un DNI y un sueldo.
public class Profesor {

    String nombreP;
    String dniP;
    double sueldo;

    Profesor(String nombreP, String dniP, double sueldo){
        this.nombreP = nombreP;
        this.dniP = dniP;
        this.sueldo = sueldo;
    }

}
